var currentLang = localStorage.getItem('lang') || 'zh';

function initLanguage() {
    currentLang = localStorage.getItem('lang') || 'zh';
    document.documentElement.lang = currentLang;
    
    updateNavItems();
    updateLangButtons();
    updateFooter();
    
    if (typeof updatePageContent === 'function') {
        updatePageContent(currentLang);
    }
}

function switchLang(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;
    
    updateNavItems();
    updateLangButtons();
    updateFooter();
    
    if (typeof updatePageContent === 'function') {
        updatePageContent(lang);
    }
}

function updateNavItems() {
    var navItems = document.querySelectorAll('.nav-item[data-lang-key]');
    navItems.forEach(function(item) {
        var key = item.dataset.langKey;
        if (key && translations[currentLang].nav[key]) {
            item.textContent = translations[currentLang].nav[key];
        }
    });
}

function updateLangButtons() {
    var langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(function(btn) {
        btn.classList.remove('active');
        if (btn.dataset.lang === currentLang) {
            btn.classList.add('active');
        }
        var langKey = btn.dataset.lang;
        if (translations[currentLang].lang[langKey]) {
            btn.textContent = translations[currentLang].lang[langKey];
        }
    });
}

function updateFooter() {
    var footerElements = document.querySelectorAll('[data-lang-role="footer"]');
    footerElements.forEach(function(el) {
        var key = el.dataset.langKey;
        if (key && translations[currentLang].footer[key]) {
            if (Array.isArray(translations[currentLang].footer[key])) {
                el.innerHTML = translations[currentLang].footer[key].map(function(item) {
                    return '<li>' + item + '</li>';
                }).join('');
            } else {
                el.textContent = translations[currentLang].footer[key];
            }
        }
    });
}

document.addEventListener('DOMContentLoaded', function() {
    updateNavItems();
    updateLangButtons();
    updateFooter();
});

window.addEventListener('load', function() {
    if (typeof updatePageContent === 'function') {
        updatePageContent(currentLang);
    }
});